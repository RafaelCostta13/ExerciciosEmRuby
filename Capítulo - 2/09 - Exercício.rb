=begin
	09 - Faça um programa que calcule e mostre a área de um triângulo.
	Sabe-se que: Área = (base * altura) / 2
=end

puts "Digite a base do triângulo :"
base = gets.to_f

puts "Digite a altura do triângulo :"
altura = gets.to_f

area = (base * altura) / 2
puts "A área do triângulo é : #{area}"

