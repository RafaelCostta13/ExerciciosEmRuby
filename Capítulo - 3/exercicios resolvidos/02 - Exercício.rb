	2 - Faça um programa que receba três notas,
	calcule e mostre a média aritmética entre elas.
=end

#Declarando as notas
nota1 = 9.5
nota2 = 8
nota3 = 7.5

#calculando a média aritmética e atribuindo o resultado para a variável media
media = (nota1 + nota2 + nota3) / 3

#imprimindo o resultado
puts "A média aritmética das três notas são : #{media}"
